    if (itemElement != null)
      return CustomXmlPropertiesConverter.CreateModelElement(itemElement);
    return null;
  }

  public static void SetCustomXmlProperties(CustomXmlCell? openXmlElement, CustomXmlProperties? value)
  {
    if (openXmlElement != null)
