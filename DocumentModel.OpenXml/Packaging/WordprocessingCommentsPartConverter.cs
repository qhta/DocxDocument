  /// <summary>
  ///   Gets or sets the root element of this part.
  /// </summary>
  public static Comments? GetComments(WordprocessingCommentsPart? openXmlElement)
  {
    if (openXmlElement?.RootElement is DocumentFormat.OpenXml.Wordprocessing.Comments rootElement)
      return CommentsConverter.CreateModelElement(rootElement);
    return null;
  }

  public static void SetComments(WordprocessingCommentsPart? openXmlElement, Comments? value)
  {
    if (openXmlElement != null)
      if (value != null)
      {
        var rootElement = CommentsConverter.CreateOpenXmlElement<DocumentFormat.OpenXml.Wordprocessing.Comments>(value);
        if (rootElement != null)
          openXmlElement.Comments = rootElement;
      }
  }

