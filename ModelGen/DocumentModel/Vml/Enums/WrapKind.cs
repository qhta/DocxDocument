  ///   When the item is serialized out as xml, its value is &quot;tight&quot;.
  /// </summary>
  Tight,
  
  
  /// <summary>
  ///   When the item is serialized out as xml, its value is &quot;through&quot;.
  /// </summary>
  Through,
  
