namespace DocumentModel.Math;


/// <summary>
///   Function Properties.
/// </summary>
public partial class FunctionProperties: ModelElement<DXM.FunctionProperties>
{
  public FunctionProperties(): base(){ }
  
  public FunctionProperties(DX.OpenXmlElement openXmlElement): base(openXmlElement) { }
  
  public FunctionProperties(DXM.FunctionProperties openXmlElement): base(openXmlElement) { }
  
  
  /// <summary>
  ///   ControlProperties.
  /// </summary>
  [DataMember]
  public DMM.ControlProperties? ControlProperties
  {
    get
    {
      var element = _Element?.GetFirstChild<DXM.ControlProperties>();
      if (element != null)
        return ControlPropertiesConverter.CreateModelElement(element);
      return null;
    }
    set
    {
      var itemElement = _ExistingElement.GetFirstChild<DXM.ControlProperties>();
      if (itemElement != null)
        itemElement.Remove();
      if (value != null)
      {
        itemElement = ControlPropertiesConverter.CreateOpenXmlElement<DXM.ControlProperties>(value);
        if (itemElement != null)
          _ExistingElement.AddChild(itemElement);
      }
    }
  }
  
}
