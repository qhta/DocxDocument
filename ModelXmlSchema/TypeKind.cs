namespace ModelXmlSchema;

/// <summary>
/// Enumerates the different kinds of types.
/// </summary>
public enum TypeKind
{
  /// <summary>
  /// A simple type.
  /// </summary>
  Simple,

  /// <summary>
  /// Simple enumeration type.
  /// </summary>
  Enum,

  /// <summary>
  /// Simple union type.
  /// </summary>
  Union,

  /// <summary>
  /// Simple List type.
  /// </summary>
  List,

  /// <summary>
  /// Complex type
  /// </summary>
  Complex
}
