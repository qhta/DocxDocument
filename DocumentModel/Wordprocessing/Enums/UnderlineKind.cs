namespace DocumentModel.Wordprocessing;

/// <summary>
///   Defines the UnderlineValues enumeration.
/// </summary>
public enum UnderlineKind
{
  /// <summary>
  ///   Single Underline.
  /// </summary>
  Single,

  /// <summary>
  ///   Underline Non-Space Characters Only.
  /// </summary>
  Words,

  /// <summary>
  ///   Double Underline.
  /// </summary>
  Double,

  /// <summary>
  ///   Thick Underline.
  /// </summary>
  Thick,

  /// <summary>
  ///   Dotted Underline.
  /// </summary>
  Dotted,

  /// <summary>
  ///   Thick Dotted Underline.
  /// </summary>
  DottedHeavy,

  /// <summary>
  ///   Dashed Underline.
  /// </summary>
  Dash,

  /// <summary>
  ///   Thick Dashed Underline.
  /// </summary>
  DashedHeavy,

  /// <summary>
  ///   Long Dashed Underline.
  /// </summary>
  DashLong,

  /// <summary>
  ///   Thick Long Dashed Underline.
  /// </summary>
  DashLongHeavy,

  /// <summary>
  ///   Dash-Dot Underline.
  /// </summary>
  DotDash,

  /// <summary>
  ///   Thick Dash-Dot Underline.
  /// </summary>
  DashDotHeavy,

  /// <summary>
  ///   Dash-Dot-Dot Underline.
  /// </summary>
  DotDotDash,

  /// <summary>
  ///   Thick Dash-Dot-Dot Underline.
  /// </summary>
  DashDotDotHeavy,

  /// <summary>
  ///   Wave Underline.
  /// </summary>
  Wave,

  /// <summary>
  ///   Heavy Wave Underline.
  /// </summary>
  WavyHeavy,

  /// <summary>
  ///   Double Wave Underline.
  /// </summary>
  WavyDouble,

  /// <summary>
  ///   No Underline.
  /// </summary>
  None
}